package model;

public class MatchScore {
    Integer score1;
    Integer score2;

    public MatchScore(Integer score1, Integer score2) {
        this.score1 = score1;
        this.score2 = score2;
    }

    public Integer getScore1() {
        return score1;
    }

    public Integer getScore2() {
        return score2;
    }
}
